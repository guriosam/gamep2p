	public void drawMap(Graphics2D screen) throws IOException {
		Tiles tiles = new Tiles();

		for (int i = 0; i < map.length; i++) {
			for (int j = 0; j < map[0].length; j++) {
				screen.drawImage(tiles.getTile(map[i][j]), Values.SCALE * j, Values.SCALE * i, null);
			}
		}
	}

	public static int getPosition(double posX, double posY) {
		if (posX > map.length || posX < 0) {
			return 0;
		}
		if (posY < 0 || posY > map[0].length) {
			return 0;
		}
		return map[(int) posX][(int) posY];
	}

